class Eventos {
    constructor(id, nombre, precio, descripción, fecha, hora, lugar, vacantes, imagen) {
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.descripción = descripción;
        this.fecha = fecha;
        this.hora = hora;
        this.lugar = lugar;
        this.vacantes = vacantes;
        this.imagen = imagen;
    }
}